  CDC_Transmit_FS("USB START\n", 10);

  HAL_Delay(5000);




  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);    //starts PWM on CH2 pin
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);

  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);    //starts PWM on CH2 pin
